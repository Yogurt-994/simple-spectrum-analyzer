		  /* 六路AD采集 */
		  CalCircuitParam(&sys);
		  /* 电路参数显示 */
		  //显示输入阻抗，输入阻抗，增益
          if(sys.inputRes < 1000)
                SetTFTText(0,7,"%.3fΩ",sys.inputRes);
          else
                SetTFTText(0,7,"%.3fkΩ",sys.inputRes*0.001f);
          if(sys.outputRes < 1000)
                SetTFTText(0,8,"%.3fΩ",sys.outputRes);
          else
                SetTFTText(0,8,"%.3fkΩ",sys.outputRes*0.001f);
          SetTFTText(0,9,"%.2f",sys.gain);

//          CheckAmp(0);
		  CheckAmp(2);
		  if(sys.AmpFreqFlag){
			  relay2Load;
			  SweepTest();			//扫频测试
			  sys.AmpFreqFlag=0;
			  relay2NoLoad;
		  }
